package coreJava;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("*\n**\n***\n****\n*****\n\n");//left half pyramid
        System.out.println("*****\n****\n***\n**\n*\n\n");//reverse half pyramid
        System.out.println("    *\n   **\n  ***\n ****\n*****");//right half pyramid
    }

}
